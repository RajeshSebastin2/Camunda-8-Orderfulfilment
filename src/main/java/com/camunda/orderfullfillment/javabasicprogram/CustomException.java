public class CustomException {

	public static void main(String[] args) {

		int j = 100, k = 0;

		int l=0;

		for(int i=0; i<=k; i++) {
			
			
		
		try {
			l = j / k;

		} catch (Exception e) {
			
			
		 	k=2;
		}
		
		System.out.println(l);

		}
		
		
	}

}
